package org.livingdoc.example

class Calculator {

    fun sum(a: Float, b: Float): Float {
        return a + b
    }

    fun diff(a: Float, b: Float): Float {
        return a - b
    }

    fun multiply(a: Float, b: Float): Float {
        return a * b
    }

    fun divide(a: Float, b: Float): Float {
        return a / b
    }
}
